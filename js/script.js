const PDFS=[];
const SCOLORS={Mathematics:'#0066B3',Physics:'#007A53',Languages:'#6c3fd8',History:'#c0392b',Chemistry:'#16a085',Biology:'#27ae60',Geography:'#d35400',Philosophy:'#2c3e50',Technology:'#2980b9',Economics:'#a07a00'};
let curSub='All';

function buildPDFFilters(){
  const subs=['All',...new Set(PDFS.map(p=>p.s))];
  document.getElementById('pdf-subs').innerHTML=subs.map(s=>
    `<button class="pdf-sub-btn${s===curSub?' act':''}" onclick="setSub('${s}')">${s}</button>`
  ).join('');
}
function setSub(s){curSub=s;buildPDFFilters();filterPDF();}
function filterPDF(){
  const q=(document.getElementById('pdf-q')||{value:''}).value.toLowerCase();
  const cnt=document.getElementById('pdf-cnt');
  const lst=document.getElementById('pdf-list');
  const res=PDFS.filter(p=>{
    const ms=curSub==='All'||p.s===curSub;
    const mq=!q||p.t.toLowerCase().includes(q)||p.d.toLowerCase().includes(q)||p.s.toLowerCase().includes(q);
    return ms&&mq;
  });
  cnt.innerHTML=`Showing <strong>${res.length}</strong> of <strong>${PDFS.length}</strong> documents`;
  if(!res.length){lst.innerHTML='<div class="pdf-empty"><i class="fas fa-search"></i><p>No results found. Try different keywords or search on Google Scholar.</p></div>';return;}
  lst.innerHTML=res.map(p=>{
    const c=SCOLORS[p.s]||'#0066B3';
    const slug=p.t.toLowerCase().replace(/[^a-z0-9]+/g,'-');
    return`<div class="pdf-item">
      <div class="pdf-file-icon"><i class="fas fa-file-pdf"></i></div>
      <div class="pdf-meta">
        <div class="pdf-title">${p.t}</div>
        <div class="pdf-desc">${p.d}</div>
        <div class="pdf-tags">
          <span class="pdf-tag" style="background:${c}">${p.s}</span>
          <span class="pdf-tag" style="background:#546e7a">${p.pg} pages</span>
        </div>
      </div>
      <div class="pdf-btns">
        <a href="assets/pdf/${slug}.pdf" download class="btn-dl"><i class="fas fa-download"></i> Download</a>
        <button class="btn-scholar-s" onclick="scholarGo('${p.t}')"><i class="fas fa-external-link-alt"></i> Scholar</button>
      </div>
    </div>`;
  }).join('');
}
function scholarGo(term){
  const q=term||(document.getElementById('pdf-q')||{value:''}).value.trim();
  if(!q){showToast('Enter a search term first','err');return;}
  window.open(`https://scholar.google.com/scholar?q=${encodeURIComponent(q)}`,'_blank');
}

