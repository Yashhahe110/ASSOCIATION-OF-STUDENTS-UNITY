let curLang='en';
function setLang(l){
  curLang=l;
  const T=LANG[l];
  document.querySelectorAll('[data-t]').forEach(el=>{
    const k=el.getAttribute('data-t');
    if(T[k]!==undefined){
      if(el.tagName==='INPUT'||el.tagName==='TEXTAREA'){
        el.placeholder=T[k];
      } else if(el.tagName==='SPAN' || el.tagName==='DIV' || el.tagName==='P' || el.tagName==='SMALL' || el.tagName==='STRONG' || el.tagName==='EM' || el.tagName==='H1' || el.tagName==='H2' || el.tagName==='H3'){
        el.textContent=T[k];
      } else {
        el.innerHTML = T[k]; // For cases where icons are included in translation strings
      }
    }
  });
  // desktop lang buttons
  ['en','fr','mg'].forEach(x=>{
    const b=document.getElementById('lb-'+x);
    if(b) b.classList.toggle('active',x===l);
    const mb=document.getElementById('mlb-'+x);
    if(mb) mb.classList.toggle('active',x===l);
  });
}

